export const PROMPT_SUMMARY_REQUIREMENT = `
SUMMARY — 3-6 sentences. This is the prose writer's only brief for the scene. Match the register of the source material (fiction, memoir, essay, reportage, research — whatever the context shows).

Include: specific entity names (not IDs), concrete specifics (objects, dialogue, data, claims), observable consequences.
Context: time span, method, tone shifts, structural role of the scene.

AVOID in DRAMATIC registers (fiction, narrative memoir, literary reportage): inner-state verbs that stand alone (realizes, confirms, understands, suspects, decides) when used as the ONLY delta | emotional endings that only assert a feeling ("intentions", "revelations-of-feeling" with no observable consequence) | vague modifiers (face etched, expression unreadable, groundbreaking implications). In these registers, write what HAPPENED, what was SAID, what visibly CHANGED — not what was privately felt.

ACCEPTABLE in REFLECTIVE / ESSAYISTIC / INQUIRY registers: realisation-as-delta is legitimate when the realisation is NAMED (a specific new claim, a specific reframing) and ATTRIBUTED (to the author, narrator, or a source). "The narrator realises that the archive is organised by the logic of its absences, not its holdings" is a valid scene summary in an essay; "She felt something shift" is not valid in any register because it is unnamed. The test is specificity, not the presence of the word "realise".

Across all registers: avoid gestural emotion-words that are stand-ins for craft — "felt", "seemed", "somehow", "a strange sense" without a specific tether.
`;
